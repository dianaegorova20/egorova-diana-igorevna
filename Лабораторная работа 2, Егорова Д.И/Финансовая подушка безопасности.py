money_capital = 20000  # Подушка безопасности
salary = 5000  # Ежемесячная зарплата
spend = 6000  # Траты за первый месяц
increase = 0.05  # Ежемесячный рост цен
months = 0  # Счетчик месяцев

while money_capital >= 0:
    # Рассчитываем бюджет текущего месяца
    budget = money_capital + salary

    # Проверяем, хватит ли бюджета на расходы
    if budget >= spend:
        # Если бюджет хватает, вычитаем расходы
        budget -= spend
        # Обновляем подушку безопасности
        money_capital = budget
    else:
        # Если бюджет не хватает, завершаем цикл
        break

    # Увеличиваем расходы на следующий месяц из-за роста цен
    spend *= (1 + increase)
    months += 1

print("Количество месяцев, которое можно протянуть без долгов:", months)

